import java.lang.*;

public class No3_Sample04 {

public static void main(String[] args) {
        int a = 1;
        int b = 3;
        int c = 0;
 
        // OR
        c = a | b;
        System.out.println("a | b = " + c);
        
        // AND
        c = a & b;
        System.out.println("a & b = " + c);
        
        // XOR
        c = a ^ b;
        System.out.println("a ^ b = " + c);
    }
}
